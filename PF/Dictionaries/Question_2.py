# Update Dictionary Values
# Given the dictionary:
# employee = {
#     "name": "Ahmed",
#     "salary": 50000,
#     "city": "Lahore"
# }
# Update:
# Salary to 65000
# City to Islamabad
# Print the updated dictionary.


employee = {
    "name": "Ahmed",
    "salary": 50000,
    "city": "Lahore"
}
employee["salary"] = 65000
employee["city"] = "Islamabad"

print(employee)